/* 
Timmy 和 Sarah 認為他們相愛了。
但在他們住的地方，有一個傳統能證明這件事：
他們各自摘一朵花，如果其中 一朵花的花瓣數是偶數、而 另一朵是奇數，
那就代表他們真的相愛。

請撰寫一個函式，接收兩個數字，
分別代表 Timmy 和 Sarah 所摘花朵的花瓣數。
- 若其中一個是偶數、另一個是奇數 → 回傳 true（他們相愛）
- 若兩個都是偶數或都是奇數 → 回傳 false（他們不相愛）
*/


/* ==================== 我的解法 ==================== */
function lovefunc(flower1, flower2){
  return (flower1 % 2) != (flower2 % 2) ? true : false
}


/* ==================== 解法參考 ==================== */
function lovefunc(flower1, flower2){
  return flower1 % 2 !== flower2 % 2;
}


